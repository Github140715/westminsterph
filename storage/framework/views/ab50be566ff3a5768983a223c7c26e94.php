                              </p>
                            </blockquote>
            
                            <p>
                                Specifically, he said, “We are ready to cope with the new technologies like the use of dual fuel engines.” Capt Leal was referring to ships run by engines using diesel and gas fuels usually liquefied natural gas (LNG), a cleaner fuel since it emits significantly lower greenhouse gas.
                            </p>
            
                            <p>
                                Moreover, Capt. Leal said, the training center had partnered with reputable software provider for this purpose. “We are tying up with suppliers of training programs on how we can provide training to our seafarers on dual fuel,” he pointed out.
