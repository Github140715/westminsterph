                                As a strong supporter of green shipping, the world’s 5th largest shipmanagement company has setup the Wallem Maritime Training Center on Juan Luna Street in Binondo, Manila to train its seafarers on the use of latest technologies to lower emissions, consume less energy, and be more fuel efficient.
